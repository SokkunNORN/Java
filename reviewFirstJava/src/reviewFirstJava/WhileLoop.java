package reviewFirstJava;

public class WhileLoop {

	public static void main(String[] args) {
		
		/*
		  
		  ###################################
		  
		  =========== while loop ============
		  
		  ###################################
		 
		 */
		
		int i = 10;
		while (i >= 0) {
			System.out.print(i + " ");
			i--;
		}
		System.out.println();
		System.out.print("We are out of while loop.");
	}

}
